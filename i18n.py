def normalize_lang(lang):
    if not lang:
        return 'en'
    lang = str(lang).lower().strip()
    return lang if lang in SUPPORTED_LANGUAGES else 'en'


def is_rtl(lang):
    return normalize_lang(lang) in RTL_LANGUAGES


def translate(text, lang):
    if text is None:
        return ''
    src = str(text)
    lang = normalize_lang(lang)
    if lang == 'en':
        return src
    row = TRANSLATIONS.get(src)
    if row and row.get(lang):
        return row[lang]

    parts = re.split(r'(\s+)', src)
    changed = False
    for i, part in enumerate(parts):
        if not part or part.isspace():
            continue
        core = re.sub(r'^[^\w\u0600-\u06FF]+|[^\w\u0600-\u06FF]+$', '', part)
        if not core:
            continue
        candidate = TRANSLATIONS.get(core)
        if candidate and candidate.get(lang):
            parts[i] = part.replace(core, candidate[lang])
            changed = True
    if changed:
        return ''.join(parts)
    return src


def get_client_dictionary(lang):
    lang = normalize_lang(lang)
    if lang == 'en':
        return {}
    result = {}
    for key, values in TRANSLATIONS.items():
        translated = values.get(lang)
        if translated:
            result[key] = translated
    return result
